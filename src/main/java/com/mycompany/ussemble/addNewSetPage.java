    private void finishSetBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_finishSetBtnActionPerformed
    try {
    // Read the existing content from saved_cards.txt
    File file = new File("saved_cards.txt");
    BufferedReader reader = new BufferedReader(new FileReader(file));
    
    StringBuilder fileContent = new StringBuilder();
    String line;
    while ((line = reader.readLine()) != null) {
        fileContent.append(line).append("\n");
    }
    reader.close();

    // Get the deck title from the JTextField
    String deckTitle = displayDeckTitle.getText().trim();

    // Ensure the deckTitle is not empty
    if (!deckTitle.isEmpty()) {
        // Get the username from OverviewPage using its public setter/getter
        OverviewPage overviewPage = new OverviewPage(); // Assuming you need to create a new instance
        String username = overviewPage.getDisplayUsername().trim(); // Use the getter method

        // Create the Flashcards folder if it doesn't exist
        File flashcardsFolder = new File("Flashcards");
        if (!flashcardsFolder.exists()) {
            flashcardsFolder.mkdir();
        }

        // Create a folder with the username inside Flashcards
        File userFolder = new File(flashcardsFolder, username);
        if (!userFolder.exists()) {
            userFolder.mkdir();
        }

        // Create the new file in the user's folder
        File newFile = new File(userFolder, deckTitle + ".txt");
        BufferedWriter writer = new BufferedWriter(new FileWriter(newFile));

        // Write the content from saved_cards.txt into the new file
        writer.write(fileContent.toString());
        writer.close();

        // Clear the content of saved_cards.txt
        BufferedWriter fileWriter = new BufferedWriter(new FileWriter(file));
        fileWriter.write(""); // Write an empty string to clear the file
        fileWriter.close();

        // Clear the JTextField after saving
        displayDeckTitle.setText("");

        // Success message
        jOptionPane2.showMessageDialog(null, "Deck saved in " + newFile.getAbsolutePath());
    } else {
        // If the deck title is empty, show a warning
        jOptionPane2.showMessageDialog(null, "Please enter a valid deck title.");
    }

    // Clear the question and answer fields
    displayQuestion.setText(""); // Clear the question field
    displayAnswer.setText("");   // Clear the answer field

} catch (IOException e) {
    // Handle IO exceptions
    jOptionPane2.showMessageDialog(null, "Error reading or writing the file: " + e.getMessage());
}
    }//GEN-LAST:event_finishSetBtnActionPerformed

