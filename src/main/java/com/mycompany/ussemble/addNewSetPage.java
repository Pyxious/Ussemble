    private void saveCardBTn1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveCardBTn1ActionPerformed
    // Get the text from the JTextFields
    String difficulty = displayDifficulty.getText();
    String question = displayQuestion.getText();
    String answer = displayAnswer.getText();
    
    // Define the file name or path where you want to save the data
    String fileName = "saved_cards.txt";  // You can change the file path and name as needed
    
    try {
        // Create a FileWriter to write to the file (true to append)
        FileWriter writer = new FileWriter(fileName, true); // 'true' means append to the file
        
        
        // Write the data to the file in a simple format
        writer.write("Difficulty: " + difficulty + "\n");
        writer.write("Question: " + question + "\n");
        writer.write("Answer: " + answer + "\n");
        writer.write("-------------------------\n");  // Separator between entries
        
        // Close the writer
        writer.close();
        
        // Optionally, show a message to confirm the data was saved
        jOptionPane2.showMessageDialog(null, "Card saved successfully!");
        
        // Step to clear the text fields after saving
        displayQuestion.setText("");  // Clear the question field
        displayAnswer.setText("");    // Clear the answer field
        displayDifficulty.setText("");
        
    } catch (IOException e) {
        // Handle any errors that occur during file writing
        jOptionPane2.showMessageDialog(null, "Error saving card: " + e.getMessage());
    }
    }//GEN-LAST:event_saveCardBTn1ActionPerformed

    
    
